    private void jTextField1KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField1KeyPressed
        // TODO add your handling code here:
        if(evt.getKeyCode()== KeyEvent.VK_ENTER) {
            if(i < 3 && jTextField1.getText().equals("gelombang")){
                JOptionPane.showMessageDialog(rootPane, "Jawaban kamu benar!");
                this.setVisible(false);
                new stage_4().setVisible(true);
            }
            else if (i != 3) {
                JOptionPane.showMessageDialog(rootPane, "Jawaban kamu salah!");
                i++;
            }
            
            else {
                JOptionPane.showMessageDialog(rootPane, "Batas menjawab telah habis! Game Over!");
                this.setVisible(false);
                new main_menu().setVisible(true);
            }
        }
    }//GEN-LAST:event_jTextField1KeyPressed

